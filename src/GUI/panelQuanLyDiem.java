    private final void clearData() {
        int n = tableModelDiem.getRowCount() - 1;
        for(int i = n; i >= 0; i--) {
            tableModelDiem.removeRow(i);
        }
    }
    
    private final void getData() {
        try {
            List<Diem> result = ctlDiem.getDiem();
            for(int i = 0; i < result.size(); i++) {
                Object[] rows = new Object[6];

                rows[0] = result.get(i).getMaSV();
                rows[1] = result.get(i).getMaLopHocPhan();
                rows[2] = result.get(i).getDiemTP();
                rows[3] = result.get(i).getDiemCK();
                rows[4] = result.get(i).getDiemTK();
                rows[5] = result.get(i).getXepLoai();

                tableModelDiem.addRow(rows);
            }
        } catch (Exception e) {
            System.out.println(  e.toString());
        }
    }
    
    private final void getCombobox() {
        List<SinhVien> sinhVien = null;
            try {
                sinhVien = ctlSinhVien.getSinhVien();
                for(int i = 0; i < sinhVien.size(); i++){
                    cmbMaSV.addItem(sinhVien.get(i).getTenSV());
                }
            } catch (Exception e) {
        }
        
        List<LopHocPhan> lopHP = null;
            try {
                lopHP = new Controller.controllerLopHocPhan().getLopHP();
                for(int i = 0; i < lopHP.size(); i++){
                    cmbMaLopHP.addItem(lopHP.get(i).getMaLopHocPhan());
                }
            } catch (Exception e) {
        }
    }
    
