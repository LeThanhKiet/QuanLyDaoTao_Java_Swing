    private void tblBangDiemMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblBangDiemMouseClicked
        // TODO add your handling code here:
        int r = tblBangDiem.getSelectedRow();
        if(r >= 0) {
            cmbMaSV.setSelectedItem(ctlSinhVien.TenSinhVienTheoMa(tblBangDiem.getValueAt(r, 0).toString()));
            cmbMaLopHP.setSelectedItem(tblBangDiem.getValueAt(r, 1).toString());
            txtDiemThanhPhan.setText(tblBangDiem.getValueAt(r, 2).toString());
            txtdiemCuoiKy.setText(tblBangDiem.getValueAt(r, 3).toString());
            txtDiemTongKet.setText(tblBangDiem.getValueAt(r, 4).toString());
            txtXepLoai.setText(tblBangDiem.getValueAt(r, 5).toString());
            
            
            cmbMaSV.setEnabled(false);
            cmbMaLopHP.setEnabled(false);
            setButton(false);
        }
    }//GEN-LAST:event_tblBangDiemMouseClicked


