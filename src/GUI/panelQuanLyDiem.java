    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        List<SinhVien> listSinhVien = ctlSinhVien.getSinhVien();
        String maSV = listSinhVien.get(cmbMaSV.getSelectedIndex()).getMaSV();
        String maLopHP = cmbMaLopHP.getSelectedItem().toString();

        Double diemTP = Double.parseDouble(txtdiemCuoiKy.getText());
        Double diemCK = Double.parseDouble(txtDiemThanhPhan.getText());
        Double diemTK = Double.parseDouble(txtDiemTongKet.getText());
        String xepLoai = txtXepLoai.getText();
        
        Diem diem = new Diem(maSV, maLopHP, diemTP, diemCK, diemTK, xepLoai);
        
        boolean check = ctlDiem.insertData(diem);
        if(check) {
        JOptionPane.showMessageDialog(null, "Thêm điểm thành công", "Thông báo", 1);
        btnLamMoi.doClick();
        } else {
            JOptionPane.showMessageDialog(null, "Thêm thất bại \nVui lòng kiểm tra lại thông tin", "Thông báo", 1);
        }
    }//GEN-LAST:event_btnThemActionPerformed

