    private void tblKhoaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblKhoaMouseClicked
        // TODO add your handling code here:
        int r = tblKhoa.getSelectedRow();
        if(r >= 0) {
            txtMaKhoa.setText(tblKhoa.getValueAt(r, 0).toString());
            txtTenKhoa.setText(tblKhoa.getValueAt(r, 1).toString());
            txtSDT.setText(tblKhoa.getValueAt(r, 2).toString());
            
            txtMaKhoa.setEnabled(false);
            setButton(false);
        }
    }//GEN-LAST:event_tblKhoaMouseClicked


