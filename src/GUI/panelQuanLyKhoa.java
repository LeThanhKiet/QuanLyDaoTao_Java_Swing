    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:
        Khoa kh = new Khoa();
        kh.setMaKhoa(txtMaKhoa.getText());
        kh.setTenKhoa(txtTenKhoa.getText());
        kh.setSDT(txtSDT.getText());

        boolean check = ctlKhoa.updateData(kh);
        
        if(check) {
            JOptionPane.showMessageDialog(null, "Sửa thành công", "Thông báo", 1);
            btnLamMoi.doClick();
        } else {
            JOptionPane.showMessageDialog(null, "Sửa thất bại", "Thông báo", 1);
        }
    }//GEN-LAST:event_btnSuaActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        int reply = JOptionPane.showConfirmDialog(null, "Bạn có chắc muốn xoá khoa này", "Thông báo", JOptionPane.YES_NO_OPTION);
        if(reply == JOptionPane.YES_OPTION) {
            boolean check = ctlKhoa.deteleData(txtMaKhoa.getText());
            if(check) {
                JOptionPane.showMessageDialog(null, "Xóa thành công", "Thông báo", 1);
                btnLamMoi.doClick();
            } else {
                JOptionPane.showMessageDialog(null, "Không thể xoá khoa, còn tồn tại chuyên ngành thuộc khoa", "Thông báo", 1);
            }
        }    
    }//GEN-LAST:event_btnXoaActionPerformed

    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        if(txtMaKhoa.getText().equals("") || txtTenKhoa.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Mã khoa và tên khoa không được trống", "Thông báo", 1);
        }
        else {
            Khoa kh = new Khoa();
            kh.setMaKhoa(txtMaKhoa.getText());
            kh.setTenKhoa(txtTenKhoa.getText());
            kh.setSDT(txtSDT.getText());
            boolean check = ctlKhoa.insertData(kh);

            if(check) {
                JOptionPane.showMessageDialog(null, "Thêm thành công", "Thông báo", 1);
                btnLamMoi.doClick();
            } else {
                JOptionPane.showMessageDialog(null, "Thêm thất bại \n Vui lòng kiểm tra lại thông tin đã nhập", "Thông báo", 1);
            }
        }
    }//GEN-LAST:event_btnThemActionPerformed

