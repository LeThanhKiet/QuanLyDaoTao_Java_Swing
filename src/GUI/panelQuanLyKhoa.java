    private final void clearData() {
        int n = tableModelKhoa.getRowCount() - 1;
        for(int i = n; i >= 0; i--) {
            tableModelKhoa.removeRow(i);
        }
    }
    
    private final void getData() {
        try {
            List<Khoa> result = ctlKhoa.getKhoa();
            for(int i = 0; i < result.size(); i++){
                Object[] rows = new Object[3];
            
                rows[0] = result.get(i).getMaKhoa();
                rows[1] = result.get(i).getTenKhoa();
                rows[2] = result.get(i).getSDT();
                
                tableModelKhoa.addRow(rows);
            }
        } catch (Exception e) {
            System.out.println(e.toString());
        }
    }
    
