    private final void clearData() {
        int n = tableModelLop.getRowCount() - 1;
        for(int i = n; i >= 0; i--) {
            tableModelLop.removeRow(i);
        }
    }
    
    private final void getData() {
        try {
            List<Lop> result = ctlLop.getLop();
            for(int i = 0; i < result.size(); i++){
                Object[] rows = new Object[5];
            
                rows[0] = result.get(i).getMaLop();
                rows[1] = result.get(i).getTenLop();
                rows[2] = result.get(i).getMaChuyenNganh();
                rows[3] = result.get(i).getSiSo();
                rows[4] = result.get(i).getKhoaHoc();
                
                tableModelLop.addRow(rows);
            }
        } catch (Exception e) {
            System.out.println(e.toString());
        }
    }
    
    private final void getCombobox(){
        List<ChuyenNganh> result = null;
        try {
            result = ctlChuyenNganh.getChuyenNganh();
            for(int i = 0; i < result.size(); i++){
                cmbChuyenNganh.addItem(result.get(i).getTenChuyenNganh());
            }
        } catch (Exception e) {
        }
    } 
    
