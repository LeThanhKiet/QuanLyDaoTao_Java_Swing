    private void btnTimKiemMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnTimKiemMouseClicked
        // TODO add your handling code here:
        clearData();
        List<Lop> listLop = ctlLop.searchData(txtTimKiem.getText());
        if(listLop != null) {
            
            for(int i = 0; i < listLop.size(); i++){
            Object[] rows = new Object[5];

            rows[0] = listLop.get(i).getMaLop();
            rows[1] = listLop.get(i).getTenLop();
            rows[2] = listLop.get(i).getMaChuyenNganh();
            rows[3] = listLop.get(i).getSiSo();
            rows[4] = listLop.get(i).getKhoaHoc();

            tableModelLop.addRow(rows);
            }
        }
    }//GEN-LAST:event_btnTimKiemMouseClicked


